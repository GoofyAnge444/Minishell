#include "libft.h"

void	ft_print_tab(char *tab_name, char **tab)
{
	size_t	i;

	i = 0;
	while (tab[i])
	{
		ft_printf("%s[%i] = %s\n", tab_name, (int)i, tab[i]);
		i++;
	}
}